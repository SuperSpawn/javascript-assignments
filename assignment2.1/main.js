
function countryInfo(country, population, capital) {
    return `${country} has ${population} people and its capital city is ${capital}`;
}

console.log(countryInfo("Germany", "83.2 million", "Berlin"));
console.log(countryInfo("Morroco", "37.08 million", "Rabat"));
console.log(countryInfo("Canada", "38.25 million", "Ottawa"));



